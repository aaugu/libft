#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*temp;

	if (!lst)
		return (0);
	while (lst->next)
	{
		new = ft_lstnew(lst->content);
		temp = new;
	}
		
}
